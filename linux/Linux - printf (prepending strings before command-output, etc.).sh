#!/bin/sh
#
# ------------------------------------------------------------

find "/var/log/" \
-type f \
-iname syslog* \
-exec printf "$(date --utc +'%Y-%m-%dT%H:%M:%S.%NZ';) $(whoami)@$(hostname) | " \; \
-exec echo "------------------------------------------------------------" \; \
-exec cat '{}' \; \
-exec echo "------------------------------------------------------------" \; \
;

