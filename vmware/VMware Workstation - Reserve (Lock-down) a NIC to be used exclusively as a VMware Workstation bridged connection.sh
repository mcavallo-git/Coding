
Similar to setting a static IP in windows, open "View Network Connections" (or run "ncpa.cpl")
 > Right click the connection you wish to defined as a VMware Workstation Bridged -> Select 'Properties'
  > Uncheck all items EXCEPT "VMware Bridge Protocol"
   > Hit OK and you should be all set


