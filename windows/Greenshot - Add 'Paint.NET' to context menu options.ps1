#  Greenshot - Add 'Paint.NET' to context menu options
#   |
#   |--> Right click the Greenshot Icon in the Tray on the Taskbar (bottom right of screen, next to clock)
#         |
#         |--> Click "Configure external commands"
#         |     |
#         |     |--> Use the "Add" & "Delete" bottons on the popup window to configure context menu options as-intended
#         |
#         |--> Troubleshooting:  If the "Configure external commands" context menu option doesn't exist, uninstall Greenshot & reinstall making sure to enable (check the checkbox) the option the option "Open with external command plug-in" at the screen titled "Select Components"
