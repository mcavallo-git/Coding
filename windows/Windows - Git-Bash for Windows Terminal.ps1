#  Git Bash in Windows Terminal  #
# ------------------------------ #

Open "Windows Terminal" > "⚙️ Settings" > "+ Add a new profile" > Enter the following:

Name:  Git Bash
Command Line:  "C:\Program Files\Git\bin\bash.exe" --login -i
Starting Directory:   %USERPROFILE%\Desktop
Icon:   "C:\Program Files\Git\mingw64\share\git\git-for-windows.ico"


